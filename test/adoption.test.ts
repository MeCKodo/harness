function manifest(repo: string): void {
  mkdirSync(join(repo, ".agents"), { recursive: true });
  writeFileSync(
    join(repo, ".agents/manifest.yaml"),
    "spec: ai-harness/v0\nidentity: { name: adoption, summary: adoption fixture }\n",
  );
}

function preparedScenario(verdict: "pass" | "fail" = "pass") {
  const repo = mkdtempSync(join(tmpdir(), "hk-adoption-receipt-repo-"));
  const external = mkdtempSync(join(tmpdir(), "hk-adoption-receipt-external-"));
  const candidate = join(external, "candidate");
  const report = join(external, "audit-report.json");
  writeFileSync(join(repo, "AGENTS.md"), "# Original rules\n\nPreserve this exact text.\n");
  symlinkSync("AGENTS.md", join(repo, "CLAUDE.md"));
  manifest(repo);
  captureLegacyEntries(repo);
  const metadata = prepareAdoptionCandidate(repo, candidate);
  writeFileSync(report, JSON.stringify({ verdict, notes: "independent comparison completed" }) + "\n", { mode: 0o600 });
  const recorded = recordAdoptionAudit(repo, candidate, {
    verdict,
    reason: "Compared every legacy entry with the generated candidate",
    reportPath: report,
  });
  const inspections = inspectManagedFiles(repo, renderTargets(loadManifest(repo)));
  return { repo, external, candidate, report, metadata, receipt: recorded.receiptPath, inspections };
}

test("legacy entry capture preserves bytes, hashes, and symlink topology", () => {
  const repo = mkdtempSync(join(tmpdir(), "hk-adoption-"));
  const original = "# Existing agent rules\n\nNever lose this rule.\n";
  writeFileSync(join(repo, "AGENTS.md"), original);
  symlinkSync("AGENTS.md", join(repo, "CLAUDE.md"));

  const index = captureLegacyEntries(repo);
  assert.equal(index.entries.length, 2);
  const agents = index.entries.find((entry) => entry.path === "AGENTS.md")!;
  assert.equal(agents.type, "file");
  assert.equal(agents.mode, 0o644);
  assert.ok(agents.sha256);
  assert.equal(readFileSync(join(repo, agents.snapshot!), "utf8"), original);
  const claude = index.entries.find((entry) => entry.path === "CLAUDE.md")!;
  assert.equal(claude.type, "symlink");
  assert.equal(claude.linkTarget, "AGENTS.md");
  assert.equal(lstatSync(join(repo, "CLAUDE.md")).isSymbolicLink(), true);
  assert.deepEqual(readAdoptionIndex(repo), index);
  assert.equal(verifyAdoptionEntry(repo, agents), true);
  assert.equal(verifyAdoptionEntry(repo, claude), true);
});

