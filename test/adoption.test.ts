test("sync refuses snapshot-only takeover and accepts the exact candidate plus pass receipt", () => {
  const repo = mkdtempSync(join(tmpdir(), "hk-adoption-sync-"));
  const external = mkdtempSync(join(tmpdir(), "hk-adoption-sync-external-"));
  writeFileSync(join(repo, "AGENTS.md"), "# Important existing rules\n");
  writeFileSync(join(repo, "CLAUDE.md"), "@AGENTS.md\n");
  manifest(repo);

  assert.throws(() => syncCmd(repo), /prepare-adoption|candidate/i);
  assert.equal(readFileSync(join(repo, "AGENTS.md"), "utf8"), "# Important existing rules\n");

  captureLegacyEntries(repo);
  assert.throws(() => syncCmd(repo, { adoptExisting: true }), /candidate|receipt/i);
  const candidate = join(external, "candidate");
  const report = join(external, "audit.json");
  prepareAdoptionCandidate(repo, candidate);
  writeFileSync(report, "{\"verdict\":\"pass\"}\n");
  const receipt = recordAdoptionAudit(repo, candidate, {
    verdict: "pass",
    reason: "Blind comparison preserved the original rule",
    reportPath: report,
  }).receiptPath;
  syncCmd(repo, { adoptExisting: true, adoptionCandidate: candidate, adoptionAudit: receipt });
  assert.match(readFileSync(join(repo, "AGENTS.md"), "utf8"), /GENERATED by ai-harness/);
  assert.equal(readFileSync(join(repo, ".agents/adoption/legacy/AGENTS.md.pre-harness"), "utf8"), "# Important existing rules\n");
});

